     <h5 class="subsection">Domande sul monitoraggio e sulla pagina personale</h5>
     
     <div style="padding-top:8px; line-height:1,5em;">
     <a name="17"></a><p><strong>Come funziona il monitoraggio? </strong></p>
     Dopo esserti registrato e una volta che sei entrato nel sito (login), comparir&agrave; nella testata - sotto il tuo nome utente - la scritta "monitoraggio" che da accesso alle tue pagine personali per l'aggiornamento e la gestione delle informazioni che ti interessano. Queste pagine, inizialmente prive di contenuti, si popolano di informazioni man mano che aggiungi elementi al tuo monitoraggio: parlamentari, atti, argomenti. 
     </div>
     <div style="padding-top:8px; line-height:1,5em;">
     <a name="18"></a><p><strong>Come posso monitorare un parlamentare? </strong></p>
     Nella pagina di ogni deputato e senatore in alto a destra trovi il bottone che ti permette di avviare il monitoraggio, contrassegnata dalla icona della lente di ingrandimento. Una volta scelto di seguire un parlamentare riceverai tutte le notizie e informazioni relative - ogni volta che ce ne saranno - sia nelle tue pagine personali del "monitoraggio" ("le mie notizie" - "i tuoi parlamentari") che nella tua casella email attraverso la "newsletter". Per smettere di monitorare l'atto o il parlamentare &egrave; sufficiente andare nella pagina dell'atto o parlamentare dove in alto a destra si trova il bottone "smetti di monitorare". La stessa funzione &egrave; disponibile anche nella pagina "i miei parlamentari" dove per ciascun deputato e senatore elencato &egrave; disponibile il bottone "smetti di monitorare" (icona della crocetta rossa accanto la lente di ingrandimento).
     </div>
      <div style="padding-top:8px; line-height:1,5em;">
     <a name="19"></a><p><strong>Come posso monitorare un atto?  </strong></p>
     Nella pagina di ogni atto (disegno di legge, decreto, mozione, interrogazione, etc.) in alto a destra trovi il bottone che ti permette di avviare il monitoraggio, contrassegnata dalla icona della lente di ingrandimento. Una volta scelto di seguire un atto riceverai tutte le notizie e informazioni relative - ogni volta che ce ne saranno - sia nelle tue pagine personali del "monitoraggio" ("le mie notizie" - "i miei atti") che nella tua casella e-mail attraverso la "newsletter" . Per smettere di monitorare l'atto &egrave; sufficiente andare nella pagina dell'atto dove in alto a destra si trova il bottone "smetti di monitorare". La stessa funzione &egrave; disponibile anche nella pagina "i miei atti" dove per ciascun atto elencato &egrave; disponibile il bottone "smetti di monitorare" (icona della crocetta rossa accanto la lente di ingrandimento).
     </div>
      <div style="padding-top:8px; line-height:1,5em;">
     <a name="20"></a><p><strong>Come posso monitorare un argomento? </strong></p>
     Per aggiungere un argomento agli oggetti monitorati puoi andare nella pagina dell'argomento dove in alto a destra trovi il bottone per avviare il monitoraggio. La stessa operazione la puoi fare dalla pagina "i miei argomenti" (in "monitoraggio"): fai click su una categoria e poi seleziona l'argomento che ti interessa, l'argomento si aggiunge nella lista in alto tra quelli monitorati. Per smettere di monitorare l'argomento &egrave; sufficiente andare nella pagina dell'argomento dove in alto a destra trovi il bottone "smetti di monitorare". La stessa funzione &egrave; disponibile anche nella pagina "i miei argomenti" dove il alto trovi "gli argomenti che stai monitorando". Se con il mouse vai sul singolo argomento, compare a fianco l'icona con la crocetta rossa selezionando la quale puoi cessare il monitoraggio. 
     </div>
      <div style="padding-top:8px; line-height:1,5em;">
     <a name="21"></a><p><strong>Cosa trovo in "le mie notizie"? </strong></p>
     E' la sezione del "monitoraggio" dove sono raccolte in maniera cronologica tutte le notizie che riguardano gli atti, i parlamentari e gli argomenti che hai scelto di monitorare. Usando le tendine in testa alla pagina, le notizie possono essere filtrate per "argomento", per "tipologia" di atto, per "ramo" (camera/senato), per "rilevanza". 
     </div>
     <div style="padding-top:8px; line-height:1,5em;">
     <a name="22"></a><p><strong>Cosa trovo in "i miei atti"? </strong></p>
     E' la sezione del "monitoraggio" in cui vengono elencati tutti gli atti che stai monitorando. Quindi sia gli atti che hai scelto direttamente sia quelli che monitori indirettamente perch&eacute; fanno parte di un argomento che hai deciso di monitorare.
Puoi perfezionare il tuo monitoraggio marcando come "preferiti" (stelletta gialla) gli atti che ritieni pi&ugrave; importanti o elimindo dall'elenco quelli che non ti interessano ("smetti di monitorare" icona della lente di ingrandimento accanto la crocetta rossa).
Questi ultimi saranno sempre disponibili nell'elenco degli "atti che hai scartato".
All'interno della pagina gli atti sono suddivisi per tipologia e usando le tendine in testa alla pagina, puoi filtrare i tuoi atti per "argomento", per "tipologia" di atto, per "ramo" (camera/senato), per "stato". Puoi vedere solo i preferiti o quelli che hai scartato. 
     </div>
      <div style="padding-top:8px; line-height:1,5em;">
     <a name="23"></a><p><strong>Cosa trovo in "i miei parlamentari"? </strong></p>
     Nella sezione "i tuoi parlamentari" sono elencati i senatori e deputati che stai monitorando. Nella colonna "Notizie" per ogni parlamentare sono raccolte tutte le notizie che lo riguardano (fare click sul quadratino azzurro). Se poi selezioni la crocetta rossa a fianco l'icona della lente di ingrandimento puoi smettere di monitorare il parlamentare. 
     </div>
